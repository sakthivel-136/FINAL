# mic_chat.py

import streamlit as st
import pandas as pd
import os
import pickle
import speech_recognition as sr
import pyttsx3
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.metrics.pairwise import cosine_similarity

# Constants
VECTOR_FILE = "vectorized.pkl"
CSV_FILE = "kcet.csv"
THRESHOLD = 0.8

# Text-to-speech engine
engine = pyttsx3.init()
def speak(text):
    engine.say(text)
    engine.runAndWait()

# Load or vectorize CSV
def load_or_vectorize():
    if os.path.exists(VECTOR_FILE):
        with open(VECTOR_FILE, "rb") as f:
            vectorizer, vectors, df = pickle.load(f)
    else:
        df = pd.read_csv(CSV_FILE)
        df['Question'] = df['Question'].str.strip().str.lower()
        vectorizer = TfidfVectorizer()
        vectors = vectorizer.fit_transform(df['Question'])
        with open(VECTOR_FILE, "wb") as f:
            pickle.dump((vectorizer, vectors, df), f)
    return vectorizer, vectors, df

vectorizer, vectors, df = load_or_vectorize()

# Recognize live speech input
def recognize_speech():
    recognizer = sr.Recognizer()
    with sr.Microphone() as source:
        st.info("ğŸ™ï¸ Listening... Speak now.")
        audio = recognizer.listen(source, timeout=5, phrase_time_limit=6)
    try:
        return recognizer.recognize_google(audio)
    except sr.UnknownValueError:
        return ""
    except sr.RequestError:
        return ""

# Streamlit UI
st.set_page_config(page_title="KCET Voice Bot", layout="centered")
st.title("ğŸ¤ KCET Live Voice Assistant")

if st.button("ğŸ¤ Speak Now"):
    query = recognize_speech()
    if query:
        st.success(f"ğŸ§‘ You said: {query}")
        query_vector = vectorizer.transform([query.lower()])
        similarity = cosine_similarity(query_vector, vectors)
        max_sim = similarity.max()
        max_index = similarity.argmax()

        if max_sim >= THRESHOLD:
            answer = df.iloc[max_index]['Answer']
        else:
            answer = "âŒ Sorry, I couldn't understand. Please try again."

        st.markdown(f"**ğŸ¤– Bot:** {answer}")
        speak(answer)
    else:
        st.warning("â— Couldn't understand any speech. Try again.")
